package API;

public class Ex06 {
	public static void main(String[] args) {
		int a = Math.abs(-10);
		double b = Math.abs(1.1);
		//abs = 절댓값 반환
		System.out.println(a);
		System.out.println(b);
		
		int c = Math.max(1, 9);
		double d = Math.max(1.1, 9.9);
		//max = 데이터 중 큰 수를 반환
		System.out.println(c);
		System.out.println(d);
		
	}

}
